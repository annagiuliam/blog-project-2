<template>
  <v-container>
    <v-list-item three-line>
      <v-list-item-content class="overflow-visible">
        <v-row class="d-flex justify-space-between align-center">
          <v-col>
            <div class="mb-1" :class="cathegoryFontSize">
              {{ displayedPost.category }}
            </div></v-col
          >
          <v-col class="d-flex justify-end align-center pa-1 mr-1">
            <RoundBtn @click="deletePost" tooltipText="lÃ¶schen">
              <v-icon>mdi-delete-outline</v-icon>
            </RoundBtn>
            <RoundBtn @click="editPost" tooltipText="bearbeiten">
              <v-icon>mdi-pencil</v-icon>
            </RoundBtn>
          </v-col>
        </v-row>
        <v-list-item-title class="text-h5 mb-1">
          {{ displayedPost.title }}
        </v-list-item-title>
        <v-row justify="space-between">
          <v-col>
            <v-list-item-subtitle>{{ fullName }}</v-list-item-subtitle>
          </v-col>
          <v-col>
            <v-list-item-subtitle class="text-right">{{
              formattedDate
            }}</v-list-item-subtitle>
          </v-col>
        </v-row>
      </v-list-item-content>
    </v-list-item>
    <v-card-text :class="textClass">{{ displayedPost.content }}</v-card-text>
  </v-container>
</template>

<script>
import RoundBtn from "./RoundBtn.vue";

export default {
  components: { RoundBtn },
  name: "PostContent",
  data() {
    return {
      dateOptions: { year: "numeric", month: "long", day: "numeric" },
    };
  },
  props: ["post", "elip"],
  computed: {
    formattedDate() {
      return this.displayedPost.creationDate.toLocaleDateString(
        undefined,
        this.dateOptions
      );
    },
    textClass() {
      let className = this.elip ? "text-elip" : "";
      return className;
    },
    cathegoryFontSize() {
      let className = this.elip ? "text-overline" : "cat-font-size";
      return className;
    },
    currentPost() {
      return this.$store.state.currentPost;
    },
    displayedPost() {
      return this.post ? this.post : this.currentPost;
    },
    fullName() {
      return `${this.displayedPost.firstName} ${this.displayedPost.middleName} ${this.displayedPost.lastName}`.trim();
    },
  },
  methods: {
    deletePost() {
      this.$store.dispatch("deletePost", this.displayedPost);

      if (this.$route.name === "post-page") {
        this.$router.push({ name: "home" });
      }
    },

    editPost() {
      this.$store.dispatch("updateCurrentPost", this.displayedPost);
      this.$store.dispatch("openInputDialog");
    },
  },
};
</script>

<style>
.text-elip {
  overflow: hidden;
  white-space: nowrap;
  text-overflow: ellipsis;
}

.cat-font-size {
  font-size: 2rem;
  text-transform: uppercase;
}
</style>
